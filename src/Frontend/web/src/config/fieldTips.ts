/**
 * Resolve a tip for a given label + current URL path. Returns undefined if
 * no entry is found — the caller should fall back to a generic message.
 */
export function lookupTip(rawLabel: string, path: string): FieldTip | undefined {
  const key = normaliseLabel(rawLabel);

  // Longest prefix wins so more specific pages override parent scopes.
  const prefixes = Object.keys(PATH_SCOPED)
    .filter((p) => path.startsWith(p))
    .sort((a, b) => b.length - a.length);
  for (const p of prefixes) {
    const hit = PATH_SCOPED[p][key];
    if (hit) return hit;
  }

  return GLOBAL[key];
}
