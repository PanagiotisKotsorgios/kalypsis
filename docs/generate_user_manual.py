def chapter(doc, num, title, intro, screenshot_label,
            how_to_open, steps, tips=None, common_issues=None):
    add_h1(doc, f"{num}. {title}")
    add_body(doc, intro)
    doc.add_paragraph()

    add_h2(doc, "Πώς το ανοίγετε")
    for step in how_to_open:
        p = doc.add_paragraph(step, style="List Bullet")
        p.paragraph_format.space_after = Pt(4)

    add_screenshot_placeholder(doc, screenshot_label)

    add_h2(doc, "Βήματα χρήσης")
    add_step_list(doc, steps)

    if tips:
        doc.add_paragraph()
        add_h2(doc, "Συμβουλές")
        for tip in tips:
            add_tip(doc, tip)

    if common_issues:
        doc.add_paragraph()
        add_h2(doc, "Συχνά ζητήματα")
        for issue in common_issues:
            add_tip(doc, issue, kind="Ζήτημα")

    add_page_break(doc)


