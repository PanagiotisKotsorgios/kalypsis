"""
Generates the Kalypsis user manual as a Word document.
All content is in Greek. Placeholder markers indicate where the operator
should paste a screenshot of the running application.
"""
from docx import Document
from docx.shared import Pt, RGBColor, Cm, Inches
from docx.enum.text import WD_ALIGN_PARAGRAPH
from docx.enum.table import WD_ALIGN_VERTICAL
from docx.oxml.ns import qn
from docx.oxml import OxmlElement

NAVY = RGBColor(0x0B, 0x25, 0x45)
ACCENT = RGBColor(0x1E, 0xA7, 0xE1)
SUBTLE = RGBColor(0x60, 0x74, 0x8E)
LIGHT_BG = "F2F6FB"


def add_screenshot_placeholder(doc, label):
    """Draws a dashed-border box telling the operator where to paste a
    screenshot from the running app."""
    table = doc.add_table(rows=1, cols=1)
    table.autofit = False
    cell = table.rows[0].cells[0]
    tc_pr = cell._tc.get_or_add_tcPr()
    # background shading
    shd = OxmlElement("w:shd")
    shd.set(qn("w:val"), "clear")
    shd.set(qn("w:color"), "auto")
    shd.set(qn("w:fill"), LIGHT_BG)
    tc_pr.append(shd)
    # dashed border
    borders = OxmlElement("w:tcBorders")
    for side in ("top", "left", "bottom", "right"):
        b = OxmlElement(f"w:{side}")
        b.set(qn("w:val"), "dashed")
        b.set(qn("w:sz"), "12")
        b.set(qn("w:color"), "1EA7E1")
        borders.append(b)
    tc_pr.append(borders)

    para = cell.paragraphs[0]
    para.alignment = WD_ALIGN_PARAGRAPH.CENTER
    run = para.add_run(f"[Στιγμιότυπο εικόνας: {label}]\n")
    run.bold = True
    run.font.size = Pt(11)
    run.font.color.rgb = ACCENT
    p2 = cell.add_paragraph()
    p2.alignment = WD_ALIGN_PARAGRAPH.CENTER
    hint = p2.add_run("Ανοίξτε την οθόνη στην εφαρμογή και εισάγετε εδώ ένα στιγμιότυπο (Insert → Pictures).")
    hint.font.size = Pt(9)
    hint.font.color.rgb = SUBTLE
    # Add some vertical breathing room
    cell.paragraphs[0].paragraph_format.space_before = Pt(24)
    cell.add_paragraph().paragraph_format.space_after = Pt(24)
    doc.add_paragraph()  # spacing after box


def add_step_list(doc, steps):
    for i, step in enumerate(steps, 1):
        p = doc.add_paragraph(style="List Number")
        p.add_run(step).font.size = Pt(12)


def add_tip(doc, text, kind="Συμβουλή"):
    p = doc.add_paragraph()
    p.paragraph_format.left_indent = Cm(0.5)
    run = p.add_run(f"{kind}: ")
    run.bold = True
    run.font.color.rgb = ACCENT
    run.font.size = Pt(11)
    tail = p.add_run(text)
    tail.font.size = Pt(11)
    tail.italic = True


def add_h1(doc, text):
    p = doc.add_paragraph()
    p.paragraph_format.space_before = Pt(24)
    p.paragraph_format.space_after = Pt(12)
    run = p.add_run(text)
    run.bold = True
    run.font.size = Pt(26)
    run.font.color.rgb = NAVY
    # Add an underline via bottom border
    p_pr = p._p.get_or_add_pPr()
    p_bdr = OxmlElement("w:pBdr")
    bottom = OxmlElement("w:bottom")
    bottom.set(qn("w:val"), "single")
    bottom.set(qn("w:sz"), "12")
    bottom.set(qn("w:color"), "1EA7E1")
    bottom.set(qn("w:space"), "4")
    p_bdr.append(bottom)
    p_pr.append(p_bdr)


def add_h2(doc, text):
    p = doc.add_paragraph()
    p.paragraph_format.space_before = Pt(18)
    p.paragraph_format.space_after = Pt(6)
    run = p.add_run(text)
    run.bold = True
    run.font.size = Pt(18)
    run.font.color.rgb = NAVY


def add_body(doc, text):
    p = doc.add_paragraph()
    p.paragraph_format.space_after = Pt(6)
    run = p.add_run(text)
    run.font.size = Pt(12)


def add_page_break(doc):
    doc.add_page_break()


def build_cover(doc):
    for _ in range(4):
        doc.add_paragraph()
    logo_p = doc.add_paragraph()
    logo_p.alignment = WD_ALIGN_PARAGRAPH.CENTER
    logo_run = logo_p.add_run("KALYPSIS")
    logo_run.bold = True
    logo_run.font.size = Pt(72)
    logo_run.font.color.rgb = NAVY

    sub_p = doc.add_paragraph()
    sub_p.alignment = WD_ALIGN_PARAGRAPH.CENTER
    sub_run = sub_p.add_run("Πλατφόρμα Διαχείρισης Ασφαλιστικού Γραφείου")
    sub_run.font.size = Pt(18)
    sub_run.font.color.rgb = ACCENT
    sub_run.italic = True

    for _ in range(2):
        doc.add_paragraph()

    title_p = doc.add_paragraph()
    title_p.alignment = WD_ALIGN_PARAGRAPH.CENTER
    title_run = title_p.add_run("Εγχειρίδιο Χρήστη")
    title_run.bold = True
    title_run.font.size = Pt(48)
    title_run.font.color.rgb = NAVY

    for _ in range(6):
        doc.add_paragraph()

    footer_p = doc.add_paragraph()
    footer_p.alignment = WD_ALIGN_PARAGRAPH.CENTER
    footer_run = footer_p.add_run("Έκδοση 2026.07 · www.mykalypsis.gr · info@mykalypsis.gr")
    footer_run.font.size = Pt(10)
    footer_run.font.color.rgb = SUBTLE
    add_page_break(doc)


