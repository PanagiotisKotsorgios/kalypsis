"""
Generates Word document templates for legal onboarding of Kalypsis
customers (insurance agencies). All Greek. All DRAFT — must be
reviewed by a lawyer before signing with a real customer.

Outputs (in docs/legal/):
  - DPA_Template_Greek.docx           (Data Processing Agreement, GDPR Art. 28)
  - MSA_Template_Greek.docx           (Master Service Agreement)
  - Copyright_Addendum_Greek.docx     (Content liability + copyright)
  - Data_Breach_Response_Plan.docx    (72-hour procedure)
  - Sub_Processors_Register.docx      (list of vendors + data flows)
"""
import os
from docx import Document
from docx.shared import Pt, RGBColor, Cm
from docx.enum.text import WD_ALIGN_PARAGRAPH

NAVY = RGBColor(0x0B, 0x25, 0x45)
ACCENT = RGBColor(0x1E, 0xA7, 0xE1)
RED = RGBColor(0xC7, 0x30, 0x30)

OUT_DIR = os.path.join(os.path.dirname(__file__), "legal")
os.makedirs(OUT_DIR, exist_ok=True)


def new_doc():
    doc = Document()
    for section in doc.sections:
        section.top_margin = Cm(2.2)
        section.bottom_margin = Cm(2.2)
        section.left_margin = Cm(2.4)
        section.right_margin = Cm(2.4)
    style = doc.styles["Normal"]
    style.font.name = "Calibri"
    style.font.size = Pt(11)
    return doc


def warning_block(doc, text):
    p = doc.add_paragraph()
    p.paragraph_format.left_indent = Cm(0.5)
    run = p.add_run("⚠ DRAFT — ")
    run.bold = True
    run.font.color.rgb = RED
    run.font.size = Pt(11)
    p.add_run(text).font.size = Pt(11)


def title(doc, text):
    p = doc.add_paragraph()
    p.alignment = WD_ALIGN_PARAGRAPH.CENTER
    run = p.add_run(text)
    run.bold = True
    run.font.size = Pt(22)
    run.font.color.rgb = NAVY


def subtitle(doc, text):
    p = doc.add_paragraph()
    p.alignment = WD_ALIGN_PARAGRAPH.CENTER
    run = p.add_run(text)
    run.italic = True
    run.font.size = Pt(13)
    run.font.color.rgb = ACCENT


def h1(doc, text):
    p = doc.add_paragraph()
    p.paragraph_format.space_before = Pt(18)
    p.paragraph_format.space_after = Pt(6)
    run = p.add_run(text)
    run.bold = True
    run.font.size = Pt(15)
    run.font.color.rgb = NAVY


def h2(doc, text):
    p = doc.add_paragraph()
    p.paragraph_format.space_before = Pt(12)
    p.paragraph_format.space_after = Pt(4)
    run = p.add_run(text)
    run.bold = True
    run.font.size = Pt(12)
    run.font.color.rgb = NAVY


def para(doc, text):
    p = doc.add_paragraph()
    p.paragraph_format.space_after = Pt(4)
    p.add_run(text).font.size = Pt(11)


def bullet(doc, text):
    p = doc.add_paragraph(text, style="List Bullet")
    p.paragraph_format.space_after = Pt(2)


def signature_block(doc):
    doc.add_paragraph()
    doc.add_paragraph()
    tbl = doc.add_table(rows=1, cols=2)
    left = tbl.rows[0].cells[0]
    right = tbl.rows[0].cells[1]
    for cell, party in ((left, "Για την Kalypsis"), (right, "Για το Γραφείο (Πελάτης)")):
        p = cell.paragraphs[0]
        p.add_run(party).bold = True
        cell.add_paragraph()
        cell.add_paragraph("Όνομα: ______________________________")
        cell.add_paragraph("Ιδιότητα: ______________________________")
        cell.add_paragraph("Ημερομηνία: ___/___/______")
        cell.add_paragraph("Υπογραφή: ______________________________")


